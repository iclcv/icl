#include <ICLUtils/BackendDispatching.h>

namespace icl {
  namespace utils {
    namespace detail {

      std::unordered_map<std::string, std::vector<RegistryEntry>>& globalRegistry() {
        static std::unordered_map<std::string, std::vector<RegistryEntry>> reg;
        return reg;
      }

      int addToRegistry(const std::string& key, RegistryEntry entry) {
        globalRegistry()[key].push_back(std::move(entry));
        return 0;
      }

    } // namespace detail
  } // namespace utils
} // namespace icl
